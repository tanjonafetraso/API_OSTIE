package models;

/**
 *
 * @author eqima
 */
public class FileEmpreinte {
    private String file;

    public FileEmpreinte(String file) {
        this.file = file;
    }

    public String getFile() {
        return file;
    }

    public void setFile(String file) {
        this.file = file;
    }

    @Override
    public String toString() {
        return "FileEmpreinte{" + "file=" + file + '}';
    }
    
}
